export default function Sidebar() {
    const { user, logout } = useAuth();
    const navigate = useNavigate();

    const handleLogout = () => {
        logout();
        navigate('/login');
    };

    const navItems = [
        { to: '/', icon: Icons.Home, label: 'Dashboard' },
        { to: '/property', icon: Icons.Chart, label: 'Property Overview' },
        { to: '/floors', icon: Icons.Building, label: 'Floors' },
        { to: '/rooms', icon: Icons.Door, label: 'Rooms' },
        { to: '/students', icon: Icons.Users, label: 'Students' },
        { to: '/kyc', icon: Icons.IdCard, label: 'KYC Verification' },
        { to: '/payments', icon: Icons.CreditCard, label: 'Payments' },
    ];

    const getInitials = (name) => {
        if (!name) return 'U';
        return name
            .split(' ')
            .map((n) => n[0])
            .join('')
            .toUpperCase()
            .slice(0, 2);
    };

    return (
        <aside className="sidebar">
            <div className="sidebar-header">
                <div className="sidebar-logo">
                    <img src="/logo.png" alt="BasaiSaathi" style={{ width: '32px', height: '32px' }} />
                    BasaiSaathi
                </div>
            </div>

            <nav className="sidebar-nav">
                <div className="nav-section">
                    <div className="nav-section-title">Main Menu</div>
                    {navItems.map((item) => (
                        <NavLink
                            key={item.to}
                            to={item.to}
                            className={({ isActive }) => `nav-link ${isActive ? 'active' : ''}`}
                        >
                            <item.icon />
                            {item.label}
                        </NavLink>
                    ))}
                </div>

                <div className="nav-section">
                    <div className="nav-section-title">Settings</div>
                    <NavLink
                        to="/settings"
                        className={({ isActive }) => `nav-link ${isActive ? 'active' : ''}`}
                    >
                        <Icons.Settings />
                        Hostel Settings
                    </NavLink>
                </div>
            </nav>

            <div className="sidebar-footer">
                <div className="user-info">
                    <div className="user-avatar">{getInitials(user?.full_name)}</div>
                    <div className="user-details">
                        <div className="user-name">{user?.full_name || 'User'}</div>
                        <div className="user-role">{user?.role || 'Owner'}</div>
                    </div>
                </div>
                <button
                    className="btn btn-ghost"
                    onClick={handleLogout}
                    style={{ width: '100%', marginTop: '8px' }}
                >
                    <Icons.Logout />
                    Sign Out
                </button>
            </div>
        </aside>
    );
}
